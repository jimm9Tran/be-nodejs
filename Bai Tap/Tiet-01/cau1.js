    var x = 5;
    var y = 3;

    // addition
    console.log("x + y = ", x + y);

    // subtraction
    console.log("x - y = ", x - y);

    // multiplication
    console.log("x * y = ", x * y);

    // division
    console.log("x / y = ", x / y);

    // remainder
    console.log("x % y = ", x % y);

    console.log("\nincrement");
    console.log("++x = ", ++x);
    console.log("x++ = ", x++);
    console.log("x = ", x);

    console.log("\ndecremen");
    console.log("--x = ", --x);
    console.log("x-- = ", x--);
    console.log("x = ", x);

    console.log("\nexponentiation");
    console.log("x ** y =", x ** y);

